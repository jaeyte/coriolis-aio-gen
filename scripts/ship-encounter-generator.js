/**
 * Ship encounter generator for Coriolis: The Third Horizon.
 *
 * Generates enemy ship actors with embedded weapon modules and
 * key crew NPC actors (captain, pilot, gunner) for space combat.
 */

import { SHIP_CLASSES, SHIP_MODULES } from "./data/ships.js";
import {
  ENEMY_SHIPS,
  SHIP_CATEGORIES,
  SHIP_ENCOUNTER_TEMPLATES,
  CREW_TEMPLATES,
  FACTION_SHIP_NAMES
} from "./data/ship-encounters.js";
import { DIFFICULTY_TIERS, ENEMY_WEAPONS, ENEMY_ARMOR, ENEMY_GEAR, getXPTier } from "./data/enemies.js";
import { LOOT_TABLES, LOOT_TIERS, TIER_ORDER, filterByTier, weightedPick } from "./data/loot.js";
import { resolveShipModule, generateShipName } from "./ship-generator.js";
import { resolveItem, resolveTalent } from "./compendium-resolver.js";
import { generateName } from "./data/names.js";

// ── Utility ──────────────────────────────────────────────────

function pick(arr) {
  return arr[Math.floor(Math.random() * arr.length)];
}

function randRange(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function clamp(val, lo, hi) {
  return Math.max(lo, Math.min(hi, val));
}

// ── Ship Name Generation ────────────────────────────────────

function generateEnemyShipName(faction) {
  const prefixes = FACTION_SHIP_NAMES[faction] || FACTION_SHIP_NAMES.unknown;
  const prefix = pick(prefixes);
  // Use a short ship-style suffix
  const suffixes = [
    "Runner", "Wing", "Claw", "Strike", "Star", "Blade",
    "Fang", "Veil", "Lance", "Dawn", "Fury", "Hawk"
  ];
  return `${prefix} ${pick(suffixes)}`;
}

// ── Enemy Ship Actor Creation ───────────────────────────────

const ALL_SKILLS = [
  "dexterity", "force", "infiltration", "manipulation",
  "meleecombat", "observation", "rangedcombat", "survival",
  "command", "culture", "datadjinn", "medicurgy",
  "mysticpowers", "pilot", "science", "technology"
];

/**
 * Create an enemy ship actor from a ship template with difficulty scaling.
 *
 * @param {string} shipKey - Key in ENEMY_SHIPS
 * @param {object} difficultyTier - From DIFFICULTY_TIERS
 * @param {object} xpTier - From getXPTier()
 * @returns {Promise<{shipActor: Actor, crewActors: Actor[]}>}
 */
async function createEnemyShip(shipKey, difficultyTier, xpTier) {
  const template = ENEMY_SHIPS[shipKey];
  if (!template) {
    console.warn(`coriolis-aio-gen | Unknown enemy ship key: ${shipKey}`);
    return null;
  }

  const baseClass = SHIP_CLASSES[template.baseClass];
  if (!baseClass) {
    console.warn(`coriolis-aio-gen | Unknown base class: ${template.baseClass}`);
    return null;
  }

  const category = SHIP_CATEGORIES[template.category];
  const scale = difficultyTier.statScale * category.hullScale;
  const bonus = xpTier.statBonus;

  // Roll and scale ship stats
  const hullPoints = clamp(Math.round(randRange(baseClass.hullPoints[0], baseClass.hullPoints[1]) * scale) + bonus, 2, 20);
  const armor = clamp(Math.round(randRange(baseClass.armor[0], baseClass.armor[1]) * scale) + Math.floor(bonus / 2), 0, 15);
  const speed = clamp(Math.round(randRange(baseClass.speed[0], baseClass.speed[1]) * difficultyTier.statScale), 1, 10);
  const maneuverability = clamp(Math.round(randRange(baseClass.maneuverability[0], baseClass.maneuverability[1]) * difficultyTier.statScale), 0, 10);

  // Build modules
  const embeddedItems = [];

  // Required base modules (bridge, reactor, graviton, cabins)
  for (const modKey of ["bridge", "reactor", "gravitonProjector", "cabins"]) {
    const moduleItem = await resolveShipModule(modKey);
    if (moduleItem) embeddedItems.push(moduleItem);
  }

  // Required weapons
  for (const wKey of template.requiredWeapons) {
    const moduleItem = await resolveShipModule(wKey);
    if (moduleItem) embeddedItems.push(moduleItem);
  }

  // Optional weapons (add up to category max, if luck allows)
  const remainingWeaponSlots = category.maxWeapons - template.requiredWeapons.length;
  if (remainingWeaponSlots > 0 && template.optionalWeapons.length > 0) {
    for (let i = 0; i < remainingWeaponSlots && i < template.optionalWeapons.length; i++) {
      if (Math.random() < 0.5) {
        const moduleItem = await resolveShipModule(template.optionalWeapons[i]);
        if (moduleItem) embeddedItems.push(moduleItem);
      }
    }
  }

  // Extra modules
  for (const modKey of template.extraModules) {
    const moduleItem = await resolveShipModule(modKey);
    if (moduleItem) embeddedItems.push(moduleItem);
  }

  // Ship name
  const name = generateEnemyShipName(template.faction);

  // Crew notes
  const crewList = template.crewRoles.map(r => {
    const t = CREW_TEMPLATES[r];
    return t ? t.role : r;
  }).join(", ");

  const notes = [
    `${template.name} — ${template.description}`,
    `\nFaction: ${template.faction}`,
    `\nCrew: ${crewList}`,
    "\nGenerated by Coriolis AIO Generator."
  ].join("");

  // Create ship actor
  const actorData = {
    name: `${template.name}: ${name}`,
    type: "ship",
    system: {
      class: "I",
      shipType: template.name,
      hullPoints: { value: hullPoints, min: 0, max: hullPoints },
      manueverability: { value: maneuverability, min: 0, max: 25 },
      speed: { value: speed, min: 0, max: 25 },
      armor: { value: armor, min: 0, max: 25 },
      signature: { value: 0, min: 0, max: 25 },
      maxEnergyPoints: Math.max(5, hullPoints),
      notes
    },
    items: embeddedItems
  };

  const shipActor = await Actor.implementation.create(actorData);

  // Create key crew NPCs
  const crewActors = [];
  for (const roleKey of template.crewRoles) {
    const crewTemplate = CREW_TEMPLATES[roleKey];
    if (!crewTemplate) continue;

    const crewActor = await createCrewNPC(crewTemplate, template, difficultyTier, xpTier, name, shipActor.id);
    if (crewActor) crewActors.push(crewActor);
  }

  return { shipActor, crewActors };
}

/**
 * Create a key crew NPC for an enemy ship.
 *
 * @param {object} crewTemplate - Crew role template from CREW_TEMPLATES
 * @param {object} shipTemplate - Enemy ship template from ENEMY_SHIPS
 * @param {object} difficultyTier - Difficulty settings
 * @param {object} xpTier - XP-based scaling
 * @param {string} shipName - Name of the ship this NPC serves on
 */
async function createCrewNPC(crewTemplate, shipTemplate, difficultyTier, xpTier, shipName, shipId = "") {
  const scale = difficultyTier.statScale;
  const bonus = xpTier.statBonus;

  const attributes = {};
  for (const [attr, base] of Object.entries(crewTemplate.attributes)) {
    attributes[attr] = clamp(Math.round(base * scale) + Math.floor(bonus / 2), 1, 8);
  }

  const skills = {};
  for (const skill of ALL_SKILLS) {
    const base = crewTemplate.skills[skill] || 0;
    skills[skill] = { value: clamp(Math.round(base * scale) + Math.floor(bonus / 3), 0, 8) };
  }

  const hpMax = attributes.strength + attributes.agility;
  const mpMax = attributes.wits + attributes.empathy;
  const crewName = `${crewTemplate.role} (${generateName().split(" ")[0]})`;

  // Resolve embedded items (weapons, armor, gear, talents)
  const embeddedItems = [];

  // Weapons
  if (crewTemplate.weapons) {
    for (const wKey of crewTemplate.weapons) {
      const wData = ENEMY_WEAPONS[wKey];
      if (wData) {
        const resolved = await resolveItem({ ...wData });
        embeddedItems.push(resolved);
      }
    }

    // Ammunition for ranged weapons
    const AMMO_MAP = {
      vulcanCricket: { ammoKey: "vulcanAmmo", qty: 1 },
      vulcanPistol:  { ammoKey: "vulcanAmmo", qty: 1 },
      vulcanCarbine: { ammoKey: "vulcanAmmo", qty: 2 },
      thermPistol:   { ammoKey: "thermCells", qty: 1 },
      thermRifle:    { ammoKey: "thermCells", qty: 2 }
    };
    const ammoNeeded = {};
    for (const wKey of crewTemplate.weapons) {
      const mapping = AMMO_MAP[wKey];
      if (mapping) {
        ammoNeeded[mapping.ammoKey] = (ammoNeeded[mapping.ammoKey] || 0) + mapping.qty;
      }
    }
    for (const [ammoKey, qty] of Object.entries(ammoNeeded)) {
      const ammoData = ENEMY_GEAR[ammoKey];
      if (ammoData) {
        const resolved = await resolveItem({ ...ammoData, system: { ...ammoData.system, quantity: qty } });
        embeddedItems.push(resolved);
      }
    }
  }

  // Armor
  if (crewTemplate.armor) {
    const aData = ENEMY_ARMOR[crewTemplate.armor];
    if (aData) {
      const resolved = await resolveItem({ ...aData });
      embeddedItems.push(resolved);
    }
  }

  // Gear
  if (crewTemplate.gear) {
    for (const gKey of crewTemplate.gear) {
      const gData = ENEMY_GEAR[gKey];
      if (gData) {
        const resolved = await resolveItem({ ...gData });
        embeddedItems.push(resolved);
      }
    }
  }

  // Talents
  if (crewTemplate.talents) {
    for (const tKey of crewTemplate.talents) {
      const talentItem = await resolveTalent(tKey);
      if (talentItem) embeddedItems.push(talentItem);
    }
  }

  const actorData = {
    name: crewName,
    type: "npc",
    system: {
      bio: {
        origin: "",
        upbringing: "",
        humanite: false,
        concept: `${shipTemplate.name} — ${crewTemplate.role}`,
        icon: "",
        groupConcept: "",
        personalProblem: "",
        appearance: { face: "", clothing: "" },
        crewPosition: { position: crewTemplate.role.toLowerCase(), shipId }
      },
      attributes: {
        strength: { value: attributes.strength },
        agility: { value: attributes.agility },
        wits: { value: attributes.wits },
        empathy: { value: attributes.empathy }
      },
      skills,
      hitPoints: { value: hpMax, max: hpMax },
      mindPoints: { value: mpMax, max: mpMax },
      experience: { value: 0 },
      radiation: { value: 0 },
      reputation: { value: 0 },
      birr: 0,
      movementRate: 10,
      notes: `${crewTemplate.role} aboard ${shipName || shipTemplate.name}.\nFaction: ${shipTemplate.faction}\n\nDifficulty-scaled by Coriolis AIO Generator.`
    },
    items: embeddedItems
  };

  return Actor.implementation.create(actorData);
}

// ── Salvage Loot Generation ─────────────────────────────────

/**
 * Salvage tiers mapped from ship category.
 */
const SALVAGE_TIER_MAP = {
  fodder: "basic",
  standard: "standard",
  heavy: "advanced",
  flagship: "elite"
};

/**
 * Generate salvage loot from defeated enemy ships.
 *
 * @param {string} highestCategory - Highest enemy ship category
 * @param {number} lootScale - Multiplier from difficulty
 * @param {number} lootBonus - Bonus from template
 * @param {number} shipCount - Number of ships defeated
 * @returns {object} { birr, items, valuables, shipParts }
 */
function generateSalvage(highestCategory, lootScale, lootBonus, shipCount) {
  const tier = SALVAGE_TIER_MAP[highestCategory] || "basic";
  const tierData = LOOT_TIERS[tier] || LOOT_TIERS.basic;
  const effectiveScale = lootScale * (1 + lootBonus) * Math.min(shipCount, 3);

  const baseBirr = randRange(tierData.birrRange[0], tierData.birrRange[1]);
  const birr = Math.round(baseBirr * effectiveScale);

  const baseCount = randRange(tierData.itemCount[0], tierData.itemCount[1]);
  const itemCount = Math.max(1, Math.round(baseCount * effectiveScale));

  const items = [];
  const valuables = [];
  const shipParts = [];

  // Ship-specific salvage components
  const SHIP_SALVAGE = [
    { name: "Salvaged Hull Plating", weight: 8 },
    { name: "Reactor Components", weight: 5 },
    { name: "Navigation Module", weight: 4 },
    { name: "Weapon Capacitors", weight: 6 },
    { name: "Sensor Components", weight: 5 },
    { name: "Emergency Rations", weight: 9 },
    { name: "Ship Computer Core", weight: 3 },
    { name: "Graviton Coils", weight: 2 },
    { name: "Engine Fuel Cells", weight: 7 },
    { name: "Communication Array Parts", weight: 6 }
  ];

  for (let i = 0; i < itemCount; i++) {
    const categoryRoll = Math.random();
    if (categoryRoll < 0.35) {
      // Ship parts (unique to salvage)
      const part = weightedPick(SHIP_SALVAGE);
      const partValue = randRange(50, 300) * (TIER_ORDER.indexOf(tier) + 1);
      shipParts.push({ name: part.name, birrValue: Math.round(partValue * lootScale) });
    } else if (categoryRoll < 0.55) {
      // Standard gear
      const pool = filterByTier(LOOT_TABLES.gear, tier);
      if (pool.length > 0) {
        const entry = weightedPick(pool);
        items.push({ name: entry.name, ...entry.item });
      }
    } else if (categoryRoll < 0.70) {
      // Weapons
      const pool = filterByTier(LOOT_TABLES.weapons, tier);
      if (pool.length > 0) {
        const entry = weightedPick(pool);
        items.push({ name: entry.name, ...entry.item });
      }
    } else {
      // Valuables
      const pool = filterByTier(LOOT_TABLES.valuables, tier);
      if (pool.length > 0) {
        const entry = weightedPick(pool);
        valuables.push({ name: entry.name, birrValue: Math.round(entry.birrValue * effectiveScale) });
      }
    }
  }

  return { birr, items, valuables, shipParts };
}

/**
 * Create a journal entry summarizing salvage from a ship encounter.
 */
async function createSalvageJournal(encounterName, salvage) {
  let html = `<h2>Salvage: ${encounterName}</h2>`;
  html += `<p><strong>Birr recovered:</strong> ${salvage.birr}</p>`;

  if (salvage.shipParts.length > 0) {
    html += `<h3>Ship Components</h3><ul>`;
    for (const part of salvage.shipParts) {
      html += `<li><strong>${part.name}</strong> — worth ~${part.birrValue} birr</li>`;
    }
    html += `</ul>`;
  }

  if (salvage.items.length > 0) {
    html += `<h3>Equipment</h3><ul>`;
    for (const item of salvage.items) {
      html += `<li><strong>${item.name}</strong> (${item.type})</li>`;
    }
    html += `</ul>`;
  }

  if (salvage.valuables.length > 0) {
    html += `<h3>Valuables</h3><ul>`;
    for (const val of salvage.valuables) {
      html += `<li><strong>${val.name}</strong> — worth ~${val.birrValue} birr</li>`;
    }
    html += `</ul>`;
  }

  const partsTotal = salvage.shipParts.reduce((s, p) => s + p.birrValue, 0);
  const valsTotal = salvage.valuables.reduce((s, v) => s + v.birrValue, 0);
  html += `<hr><p><strong>Total estimated value:</strong> ${salvage.birr + partsTotal + valsTotal} birr`;
  html += ` (${salvage.birr} cash + ${partsTotal} in parts + ${valsTotal} in valuables)</p>`;
  html += `<p><em>Generated by Coriolis AIO Generator</em></p>`;

  return JournalEntry.implementation.create({
    name: `Salvage: ${encounterName}`,
    pages: [{
      name: "Salvage Manifest",
      type: "text",
      text: { content: html, format: 1 }
    }]
  });
}

// ── Main Ship Encounter Generator ───────────────────────────

/**
 * Generate a complete ship encounter with enemy ships and crew.
 *
 * @param {object} options
 * @param {string} [options.templateKey] - Encounter template key, or random
 * @param {string} [options.difficulty="normal"] - Difficulty tier key
 * @param {number} [options.partySize=4] - Number of players (scales enemy count)
 * @param {number} [options.partyXP=0] - Average XP per player
 * @param {boolean} [options.generateSalvage=true] - Whether to generate salvage journal
 * @returns {Promise<{shipActors: Actor[], crewActors: Actor[], journal: JournalEntry|null, summary: string}>}
 */
export async function generateShipEncounter(options = {}) {
  const difficulty = options.difficulty || "normal";
  const partySize = options.partySize ?? 4;
  const partyXP = options.partyXP ?? 0;
  const shouldGenerateSalvage = options.generateSalvage !== false;

  const difficultyTier = DIFFICULTY_TIERS[difficulty] || DIFFICULTY_TIERS.normal;
  const xpTier = getXPTier(partyXP);

  // Pick template
  let templateKey = options.templateKey;
  if (!templateKey) {
    const keys = Object.keys(SHIP_ENCOUNTER_TEMPLATES);
    templateKey = keys[Math.floor(Math.random() * keys.length)];
  }
  const template = SHIP_ENCOUNTER_TEMPLATES[templateKey];
  if (!template) {
    ui.notifications.error("Unknown ship encounter template.");
    return { shipActors: [], crewActors: [], journal: null, summary: "Error: unknown template" };
  }

  // Scale enemy count by party size (baseline 4)
  const partySizeScale = partySize / 4;
  const countScale = difficultyTier.enemyCountScale * partySizeScale;

  const shipActors = [];
  const crewActors = [];
  let highestCategory = "fodder";

  for (const group of template.enemies) {
    const shipTemplate = ENEMY_SHIPS[group.shipKey];

    // Track highest category for salvage tier
    if (shipTemplate) {
      const catOrder = ["fodder", "standard", "heavy", "flagship"];
      if (catOrder.indexOf(shipTemplate.category) > catOrder.indexOf(highestCategory)) {
        highestCategory = shipTemplate.category;
      }
    }

    const baseCount = randRange(group.count[0], group.count[1]);
    const scaledCount = Math.max(group.count[0] > 0 ? 1 : 0, Math.round(baseCount * countScale));

    for (let i = 0; i < scaledCount; i++) {
      const result = await createEnemyShip(group.shipKey, difficultyTier, xpTier);
      if (result) {
        shipActors.push(result.shipActor);
        crewActors.push(...result.crewActors);
      }
    }
  }

  // Generate salvage loot journal
  let journal = null;
  if (shouldGenerateSalvage && shipActors.length > 0) {
    const salvage = generateSalvage(highestCategory, difficultyTier.lootScale, template.lootBonus, shipActors.length);
    journal = await createSalvageJournal(template.label, salvage);
  }

  // Build summary
  const shipCounts = {};
  for (const actor of shipActors) {
    const baseName = actor.name.split(":")[0].trim();
    shipCounts[baseName] = (shipCounts[baseName] || 0) + 1;
  }
  const shipList = Object.entries(shipCounts)
    .map(([name, count]) => `${count}x ${name}`)
    .join(", ");

  const summary = `${template.label} (${difficultyTier.label} / ${xpTier.label}): ${shipList} — ${crewActors.length} crew NPCs`;

  ui.notifications.info(`Ship encounter generated: ${template.label} — ${shipActors.length} ships, ${crewActors.length} crew.`);

  return { shipActors, crewActors, journal, summary, templateKey };
}
